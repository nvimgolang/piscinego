package piscine

// BasicJoin принимает слайс строк и возвращает одну строку,
// которая является конкатенацией (соединением) всех элементов слайса
func BasicJoin(elems []string) string {
	// Инициализируем пустую строку для результата
	// Здесь будем накапливать все строки из слайса
	result := ""

	// Проходим по каждой строке в слайсе elems
	// i - индекс элемента, elem - сама строка
	for _, elem := range elems {
		// Добавляем текущую строку к результату
		// Оператор += выполняет конкатенацию строк
		// result = result + elem
		result += elem
	}

	// Возвращаем итоговую объединённую строку
	return result
}
