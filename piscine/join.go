package piscine

// Join принимает слайс строк и разделитель, возвращает одну строку,
// где все элементы слайса соединены через указанный разделитель
func Join(strs []string, sep string) string {
	// Если слайс пустой, возвращаем пустую строку
	// Это важная проверка для избежания ошибок
	if len(strs) == 0 {
		return ""
	}

	// Если в слайсе только один элемент, возвращаем его без разделителя
	// Разделитель нужен только между элементами
	if len(strs) == 1 {
		return strs[0]
	}

	// Инициализируем результат первым элементом слайса
	// Это позволяет избежать лишнего разделителя в начале
	result := strs[0]

	// Проходим по остальным элементам, начиная с индекса 1
	// Каждый элемент добавляем с разделителем перед ним
	for i := 1; i < len(strs); i++ {
		// Сначала добавляем разделитель, затем текущий элемент
		result += sep + strs[i]
	}

	// Возвращаем итоговую строку
	return result
}
